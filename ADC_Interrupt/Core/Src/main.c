/*
 * Timer callback function for
 */
void HAL_TIM_PeriodElapsedCallback (TIM_HandleTypeDef *htim)
{
	if (htim->Instance == TIM3)
	{
		timer3_counter++;
		if(timer3_counter >= 600) // software counter to calculate the 10 min
		{	// 600 sec = 10 min; once timer hits 10 min; start ADC
			timer3_counter = 0;
			HAL_ADC_Start_IT(&hadc); //Starts ADC and starts conversion
		}
	}
}

/*
 * ADC conversion complete callback -
 */
void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	if (hadc->Instance == ADC1)
	{
		adc_value = HAL_ADC_GetValue(hadc);
		// STM32F070RB has a 12bit ADC; hence the maximum value the adc will be 4095 (0-4095 = 2 power 12 = 4096)
		// higher adc_value; max voltage (~3.3v or ~5v) indicates the soil dryness
		// lower adc_value; min voltage (~0v) indicates the soil wetness
		moisture_value = (float)(((ADC_MAX - adc_value)*100)/(ADC_MAX - ADC_MIN));
		if(moisture_value > 100) moisture_value = 100;
		else if (moisture_value < 0) moisture_value = 0;
		 printf("ADC Value = %d \t Moisture %% = %.2f\n", adc_value, moisture_value);

	}
}
